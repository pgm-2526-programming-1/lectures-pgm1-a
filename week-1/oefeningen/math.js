const x = 5;
const y = 3;

// optie 1
console.log(x + " + " + y + " is " + (x + y));
console.log(x + " - " + y + " is " + (x - y));
console.log(x + " * " + y + " is " + x * y);
console.log(x + " / " + y + " is " + x / y);

// optie 2
console.log(`${x} + ${y} is ${x + y}`);
console.log(`${x} - ${y} is ${x - y}`);
console.log(`${x} * ${y} is ${x * y}`);
console.log(`${x} / ${y} is ${x / y}`);
